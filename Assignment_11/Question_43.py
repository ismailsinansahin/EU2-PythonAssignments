"""
Given an array nums with 7 integers already assigned, write code to reverse it. 
Do not use any additional arrays or Strings.
Sample Output:
nums -> [4, 3, 2, 44, 1, 100, 55]
change it to:
nums -> [55, 100, 1, 44, 2, 3, 4]
"""

nums1 = [4, 3, 2, 44, 1, 100, 55]
nums2 = [55, 100, 1, 44, 2, 3, 4]

print("\nnums1 -> ", nums1)
nums1.reverse()
print("reversed -> ", nums1)

print("\nnums2 -> ", nums2)
nums2.reverse()
print("reversed -> ", nums2)
 